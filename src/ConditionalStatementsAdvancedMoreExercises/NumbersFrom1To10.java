package ConditionalStatementsAdvancedMoreExercises;

public class NumbersFrom1To10 {
    public static void main(String[] args) {
        // for loop
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        // while loop
        int i = 1;
        while (i != 11) {
            System.out.println(i);
            i++;
        }
    }
}
